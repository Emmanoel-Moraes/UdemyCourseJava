// ContaCinco.java

// Exibe contagem em 5 por meio de estrutura for


public class ContaCinco {

    // inicia execução do programa
    public static void main (String [] args){


        for (int n = 5; n >= 1; n--){

            System.out.println("Valor de n: " + n);
        }

    } // fim da main

} // fim da classe ContaCinco
