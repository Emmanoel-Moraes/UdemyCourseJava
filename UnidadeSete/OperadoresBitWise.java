// OperadoresBitWise

// Operadores bitwise são operadores para testes lógicos
// E = &
// OU = |
// OU exclusivo = ^


public class OperadoresBitWise {

    // Inicia execução do programa
    public static void main (String[] args){

        int a = 2, b = 4;

        System.out.println(a & b);
        System.out.println(a | b);
        System.out.println(a ^ b);

    } // fim da main

} // fim da classe OperadoresBitWise
