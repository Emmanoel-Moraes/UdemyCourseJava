//  Exibe os principais operadores  comparativos em java


public class OperadoresComparativos {

    // Inicia execução do programa
    public static void main (String [] args){


        System.out.println("Operador Maior: >\nEx. 8 > 2" + " resultado: " + (8 > 2) + "\n");
        System.out.println("Operador Menor: <\nEx. 8 < 2" + " resultado: " + (8 < 2) + "\n");
        System.out.println("Operador Maior ou igual: >=\nEx. 8 >= 2" + " resultado: " + (8 >= 2) + "\n");
        System.out.println("Operador Menor ou igual: <=\nEx. 8 <= 2" + " resultado: " + (8 <= 2) + "\n");
        System.out.println("Operador igual: ==\nEx. 8 == 2" + " resultado: " + (8 == 2) + "\n");
        System.out.println("Operador Diferente: !=\nEx. 8 != 2" + " resultado: " + (8 != 2) + "\n");

    } // fim da main

} // fim da classe OperadoresComparativos
